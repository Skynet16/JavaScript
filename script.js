// Напишем простую функцию для сложения двух чисел

// задаем переменные
var a,b;

// создаем функцию
function summ_it_up(a,b) {

  // выводим в консоль то что мы складываем
  console.log("Результат сложения " + a + " и " + b);

  // выводим в консоль результат сложения
  console.log(a + b);

}

// вызываем функцию, передавая в качестве аргументов числа 2 и 4
summ_it_up(2,4);

// вызываем функцию, передавая в качестве аргументов числа 1 и 1
summ_it_up(1,1);
